package algorithm;

public class Pair {

	private String first;
	private APNRs second;

	public Pair(String _first, APNRs _second) {
		first = _first;
		second = _second;
	}

	public String getFirst() {
		return first;
	}

	public void setFirst(String first) {
		this.first = first;
	}

	public APNRs getSecond() {
		return second;
	}

	public void setSecond(APNRs second) {
		this.second = second;
	}

}
